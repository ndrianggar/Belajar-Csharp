using System;

namespace Operator
{
    class Program
    {
        static void Main(string[] args)
        {
            int x = 5;
            int y = 6;

            Console.WriteLine("operator pertambahan " +
                x + y);
            Console.WriteLine("operator perkalian " +
    x * y);

            Console.WriteLine("operator pembagian  " +
  x / y);
            Console.WriteLine("operator modulus " +
  x %  y);

            Console.WriteLine("operator increment " +
  x--);
        }
    }
}
